class Cat():
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def meow(self):
        print("Meow!")

    def info(self):
        print("Name: ", self.name)

class Dog():
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def bark(self):
        print("bark!")

    def info(self):
        print("Name: ", self.name)